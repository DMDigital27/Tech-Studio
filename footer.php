(function() {
  const htmlEl = document.documentElement;
  const switcher = document.getElementById('color-switcher');

  // 1. Initial Load: Default to 'dark' or use saved preference
  const currentTheme = localStorage.getItem('theme') || 'dark';
  htmlEl.setAttribute('data-theme', currentTheme);

  // 2. Toggle Theme function
  function toggleTheme() {
    const theme = htmlEl.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    htmlEl.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
  }

  // 3. Listener for your switch button
  if (switcher) {
    switcher.addEventListener('click', (e) => {
      e.preventDefault();
      toggleTheme();
    });
  }
})();
</script>